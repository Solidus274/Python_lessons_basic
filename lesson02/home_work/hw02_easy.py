# Задача-1:
# Дан список фруктов.
# Напишите программу, выводящую фрукты в виде нумерованного списка,
# выровненного по правой стороне.

# Пример:
# Дано: ["яблоко", "банан", "киви", "арбуз"]
# Вывод:
# 1. яблоко
# 2.  банан
# 3.   киви
# 4.  арбуз

grocery = ['авокадо','сыр', 'хлеб', 'молоко', 'чай', 'вода', 'мясо']
i = 0
while len(grocery) > i:
    print(i + 1,'.', '{:>10}'.format(grocery[i]),sep = '')
    i += 1

